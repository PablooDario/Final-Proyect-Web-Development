create database ESCOM;
use ESCOM;

create table Usuario (
IDUser varchar (8)not null,
Nombre varchar(30) not null,
APP varchar(20) not null,
APM varchar(20),
Correo varchar(30) not null,
Depto varchar(10) not null,
Pwd varchar(20) not null,
Encuesta integer not null default 0,
Comprobante Varchar (500),
constraint cveUsuario primary key(IDuser));

create table materia(
IDMat varchar(5) not null, 
nombre varchar(80) not null,
semestre integer not null,
academia varchar(50) not null,
plan integer not null,
programa varchar(40) not null,
constraint cveMateria primary key(IDmat));

create table actividad(
IDact varchar(3) not null,
descripcion varchar(200) not null, 
constraint CVEact primary key(IDact)); 
 
create table Act_Elegidas(
IDprof varchar (8) not null,
IDact varchar(3) not null,
Horas int not null default 0,
constraint cveAct_Eleg primary key(IDprof, IDact),
constraint cveProf_act 
	foreign key(IDprof) references usuario (IDuser)
	on delete cascade on update cascade,
constraint cveAct_Eleg 
	foreign key(IDact) references actividad (IDact)
	on delete cascade on update cascade);
    
create table Materias_Elegidas(
IDprof varchar(8) not null, 
IDmat varchar(5) not null,
constraint cveMat_Eleg primary key(IDprof, IDmat),
constraint cveProf_mat 
	foreign key(IDprof) references usuario (IDuser)
	on delete cascade on update cascade,
constraint cveMat_Eleg 
	foreign key(IDmat) references materia (IDmat)
	on delete cascade on update cascade);

